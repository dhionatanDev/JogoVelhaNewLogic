    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
       Controle controle = new Controle();
       if(txtJogador1.getText().isEmpty()){
           JOptionPane.showMessageDialog(null,"Informe o nome do 1ยบ Jogador");
       }
       else if(txtJogador2.getText().isEmpty() ){
       JOptionPane.showMessageDialog(null,"Informe o nome do 2ยบ Jogador");
       }
       else if(Integer.parseInt((String) cBoxRodadas.getSelectedItem())==0){
       JOptionPane.showMessageDialog(null,"Informe a quantidade de rodadas");
       }
       else if(txtJogador1.getText().equals(txtJogador2.getText())){
       JOptionPane.showMessageDialog(null,"Informe nomes diferentes para os jogadores");
       }
       else{
       controle.setJogadorX(txtJogador1.getText());
       controle.setJogadorY(txtJogador2.getText());
       controle.setRodadas(Integer.parseInt((String) cBoxRodadas.getSelectedItem()));
        Tela viw = new Tela();
        viw.preenchimento(controle);
        
       viw.setVisible(true);
       
       this.dispose();
       }
       
      
        
      
    }//GEN-LAST:event_btnNovoActionPerformed

